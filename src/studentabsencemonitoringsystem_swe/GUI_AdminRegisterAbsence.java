    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // ok button
        // stroring all the input in varibles
        String firstName = jTextField2.getText();
        String lastName = jTextField3.getText();
        String studentID = jTextField1.getText();
        String dateOfAbsence = jFormattedTextField1.getText();
        try {
            //take from admin the absence info
            Absence absence = Absence.getAbsenceInfo(firstName, lastName, studentID, dateOfAbsence);

            //get Student object
            Student student = absence.getStudent();
            // exmaple
//            System.out.println("GUI_AdminRegisterAbsence "+student.getF_name());
            
            //register absence in file
            Admin.registerAbsence(absence, student);

        } catch (IOException ex) {
            Logger.getLogger(GUI_AdminRegisterAbsence.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

